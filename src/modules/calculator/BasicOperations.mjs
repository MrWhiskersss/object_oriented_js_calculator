class basicOperations {
	static addTwoValues(value0, value1) {
		return value0 + value1;
	}

	static subtractTwoValues(value0, value1) {
		return value0 - value1;
	}

	static multiplyTwoValues(value0, value1) {
		return value0 * value1;
	}

	static divideTwoValues(value0, value1) {
		return value0 / value1;
	}

}

export default basicOperations;
